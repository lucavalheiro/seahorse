// Translation helper function
function T() {
  return function(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];
    
    for (const k of keys) {
      if (value && value[k]) {
        value = value[k];
      } else {
        // Fallback to English if translation not found
        value = translations.en;
        for (const fallbackKey of keys) {
          if (value && value[fallbackKey]) {
            value = value[fallbackKey];
          } else {
            return key; // Return key if not found in English either
          }
        }
        break;
      }
    }
    
    return value || key;
  };
}

// Apply translations to the page
function applyTranslations() {
  const elements = document.querySelectorAll('[data-i18n]');
  const t = T();
  
  elements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = t(key);
    
    if (element.tagName === 'INPUT' && element.type === 'text') {
      element.placeholder = translation;
    } else {
      element.textContent = translation;
    }
  });
  
  // Handle placeholder translations
  const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
  placeholderElements.forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    const translation = t(key);
    element.placeholder = translation;
  });
}

// Language change handler
function changeLanguage(newLanguage) {
  if (translations[newLanguage]) {
    currentLanguage = newLanguage;
    applyTranslations();
    updateCurrentDate();
    
    // Save language preference
    localStorage.setItem('seahorse-language', newLanguage);
    
    // Update language selector
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
      langSelect.value = newLanguage;
    }
  }
}

// Initialize language from saved preference or browser language
function initializeLanguage() {
  const savedLanguage = localStorage.getItem('seahorse-language');
  const browserLanguage = navigator.language.split('-')[0];
  
  let initialLanguage = 'en';
  
  if (savedLanguage && translations[savedLanguage]) {
    initialLanguage = savedLanguage;
  } else if (translations[browserLanguage]) {
    initialLanguage = browserLanguage;
  }
  
  currentLanguage = initialLanguage;
  
  // Set language selector
  const langSelect = document.getElementById('langSelect');
  if (langSelect) {
    langSelect.value = initialLanguage;
  }
  
  applyTranslations();
}

// Export for global access
window.translations = translations;
window.T = T;
window.applyTranslations = applyTranslations;
window.changeLanguage = changeLanguage;
window.initializeLanguage = initializeLanguage;

